function getLang() {
  return localStorage.getItem('lekkerlandLang') || 'af';
}

function setLang(lang) {
  localStorage.setItem('lekkerlandLang', lang);
  applyLang(lang);
  document.documentElement.lang = lang;
}

function applyLang(lang) {
  document.querySelectorAll('[data-t]').forEach(el => {
    const keys = el.getAttribute('data-t').split('.');
    let value = translations;
    for (const k of keys) {
      if (value && value[k] !== undefined) value = value[k];
      else { value = null; break; }
    }
    if (value && value[lang] !== undefined) {
      el.innerHTML = value[lang];
    }
  });

  // Update toggle buttons
  document.querySelectorAll('.lang-toggle button').forEach(btn => {
    btn.classList.toggle('active', btn.dataset.lang === lang);
  });
}

// Language popup logic (only shows on first visit)
function initLanguage() {
  const saved = localStorage.getItem('lekkerlandLang');
  if (!saved) {
    const popup = document.getElementById('lang-popup');
    if (popup) popup.classList.add('show');
  } else {
    applyLang(saved);
  }
}

function chooseLang(lang) {
  const popup = document.getElementById('lang-popup');
  if (popup) popup.classList.remove('show');
  setLang(lang);
}

document.addEventListener('DOMContentLoaded', initLanguage);
